    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow=requestTable.getSelectedRow();
         if (selectedRow < 0){
             JOptionPane.showMessageDialog(null, "Please select request for granting Access.", "Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }else{
             AccessRequest accesRequest=(AccessRequest)requestTable.getValueAt(selectedRow, 0);
             accesRequest.setAccessStatus("Granted");
             accesRequest.setReceiver(userAccount);
             GrantedAccessJPanel gajp=(GrantedAccessJPanel)jTabbedPane1.getComponent(0);
             gajp.populateRequestTable();
             populateRequestTable();
         }
    }//GEN-LAST:event_jButton1ActionPerformed

