 public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) requestTable.getModel();
       // organizationJTable.setRowSorter(new TableRowSorter(model));
        model.setRowCount(0);
        if(!enterprise.getWorkQueue().getWorkRequestList().isEmpty()){
            for(WorkRequest wr:enterprise.getWorkQueue().getWorkRequestList()){
                AccessRequest ar=(AccessRequest)wr;
                if(ar.getAccessStatus().equals("Requested")){
                    Object[] row = new Object[3];
                            row[0] = ar;
                            row[1] = ar.getAccessStatus();
                            row[2] = ar.getMessage();
                            model.addRow(row);
                }
                     
            }
        }
    }
