    private void requestAccessJButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestAccessJButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow=availableToRequest.getSelectedRow();
         if (selectedRow < 0){
             JOptionPane.showMessageDialog(null, "Please select  Enteprise for requesting Access.", "Info", JOptionPane.INFORMATION_MESSAGE);
            return;
        }else{
             AccessRequest accesRequest=new AccessRequest();
             Network n=(Network)availableToRequest.getValueAt(selectedRow, 0);
             Enterprise ent=(Enterprise)availableToRequest.getValueAt(selectedRow, 1);
             PatientOrganization p=(PatientOrganization)availableToRequest.getValueAt(selectedRow, 2);
             ent.getWorkQueue().getWorkRequestList().add(accesRequest);
             userAccount.getWorkQueue().getWorkRequestList().add(accesRequest);
             accesRequest.setPatientOrganization(p);
             accesRequest.setAccessStatus("Requested");
             accesRequest.setMessage("Access requested for patient data of Enterprise-"+ent+" of Network-"+n);
             accesRequest.setReceiver(null);
             accesRequest.setSender(userAccount);
             accesRequest.setRequestDate(new Date());
             accesRequest.setStatus("Initial");
             JOptionPane.showMessageDialog(null, "Access request sent.");
             
             InitialRequests ir=(InitialRequests)jTabbedPane1.getComponent(0);
             ir.populateInitialRequestTable();
         
         
        }
         
    }//GEN-LAST:event_requestAccessJButton1ActionPerformed
 
