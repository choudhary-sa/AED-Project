    public void populateAvailabletoRequest(){
        DefaultTableModel model = (DefaultTableModel) availableToRequest.getModel();
       // organizationJTable.setRowSorter(new TableRowSorter(model));
        model.setRowCount(0);
        
        for (Network n:business.getNetworkList()){
            enterpriseDirectory=n.getEnterpriseDirectory();
            for(Enterprise e:enterpriseDirectory.getEnterpriseList()){
                for(Organization o:e.getOrganizationDirectory().getOrganizationList()){
                    if(o instanceof PatientOrganization){
                        if(!enterprise.getOrganizationDirectory().getOrganizationList().contains(o)){
                            Object[] row = new Object[3];
                            row[0] = n;
                            row[1] = e;
                            row[2] = o;
                            model.addRow(row);
                        }
                         
                    }
                }
            }
           
        }
    }
   
    
    
    
   
    
