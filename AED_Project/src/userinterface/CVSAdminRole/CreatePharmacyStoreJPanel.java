    private void saveJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveJButtonActionPerformed
        // TODO add your handling code here:
//        private Pharmacy p =new Pharmacy();
        String enteredName=storeNameJTextField.getText(); 
            
                    boolean alreadyPresent=false;
                    for(Organization org:enterprise.getOrganizationDirectory().getOrganizationList()){
                        if(org.getName().equals(enteredName)){
                            JOptionPane.showMessageDialog(null, "Pharmacy Already exist in Directory.", "Info", JOptionPane.INFORMATION_MESSAGE);
                            alreadyPresent=true;
                            break;
                        }
                    }
                    if(!alreadyPresent){
                        boolean result=validatePharmacyDetailsforCreation();
                        if(result){
                        CVSPharmacyStoreOrganization organization=(CVSPharmacyStoreOrganization) enterprise.getOrganizationDirectory().createOrganization(Organization.Type.CVSStore, enteredName);
                        
                        organization.setLocation(storeLocationJTextField.getText());
                        organization.setStoreAddress(storeAddressJTextField.getText());
                        organization.setStoreContact(Double.parseDouble(storeContactJTextField.getText()));
                        JOptionPane.showMessageDialog(null, "New Pharmacy Store Created", "Info", JOptionPane.INFORMATION_MESSAGE);
                        resetFields();
                        }else{
                            JOptionPane.showMessageDialog(null, "Please enter valid data in proper format", "Info", JOptionPane.INFORMATION_MESSAGE);
                        }
                    
                }  
    }//GEN-LAST:event_saveJButtonActionPerformed

    public void resetFields(){
        storeAddressJTextField.setText("");
        storeContactJTextField.setText("");
        storeLocationJTextField.setText("");
        storeNameJTextField.setText("");
    }
    public boolean validatePharmacyDetailsforCreation(){
        boolean result;
        try{
            storeNameJTextField.getText();
            storeLocationJTextField.getText();
            storeAddressJTextField.getText();
            Double.parseDouble(storeContactJTextField.getText());
            result=true;
        }catch(Exception e){
            result=false;
        }
        return result;
    }
