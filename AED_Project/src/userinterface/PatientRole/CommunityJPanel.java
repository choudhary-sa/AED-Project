    private void leaveJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leaveJButtonActionPerformed
        // TODO add your handling code here:
        CommunityProfile c=null;
        for(CommunityProfile cp:community.getListOfCommunityMembers()){
            if(cp.getUserAccount().getEmployee().toString().equals(userAccount.getEmployee().toString())){
                c=cp;
                break;
            }
        }
        int answer=JOptionPane.showConfirmDialog(null,"Are you sure you want to Leave?");
        if(answer==JOptionPane.YES_OPTION){
            community.leaveCommunity(c);
        joinJButton.setEnabled(true);
        }
        else{
            return;
        }
        
    }//GEN-LAST:event_leaveJButtonActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:if(!community.getListOfCommunityMembers().isEmpty()){
            boolean notPresent=false;
            boolean present=false;
        if(community.getListOfCommunityMembers().isEmpty()){
            JOptionPane.showMessageDialog(null,"No members to view");
            return;
        }else{
            for(CommunityProfile cp:community.getListOfCommunityMembers()){
            if(cp.getUserAccount().equals(userAccount)){
                present=true;
                break;
                
            }
            
        } 
            if(!present){
                JOptionPane.showMessageDialog(null,"You cannot access community without being a member.Join Community first to enter");
                    return;
            }
            if(present){
                    ViewCommunityMembersJPanel vcmjp=new ViewCommunityMembersJPanel(userProcessContainer,userAccount,community);
                userProcessContainer.add("vcmjp",new  JScrollPane(vcmjp));
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
                    
            }
        }
        
                
               
    }//GEN-LAST:event_jButton1ActionPerformed

