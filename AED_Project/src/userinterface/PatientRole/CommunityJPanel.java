    private void joinJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_joinJButtonActionPerformed
        // TODO add your handling code here:
        boolean isAlreadyJoined=false;
        
        if(!community.getListOfCommunityMembers().isEmpty()){
            for(CommunityProfile cp:community.getListOfCommunityMembers()){
            if(cp.getUserAccount().equals(userAccount)){
                isAlreadyJoined=true;
                 break;
            }
               
            }
            
        }
        if(isAlreadyJoined){
            JOptionPane.showMessageDialog(null,"You are already connected to the community");
                return;
        }
            if(!isAlreadyJoined){
                JoinCommunityJPanel joinCommunity=new JoinCommunityJPanel(userProcessContainer,userAccount,community);
        userProcessContainer.add("joinCommunity",new  JScrollPane(joinCommunity));
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
         
            }
    }//GEN-LAST:event_joinJButtonActionPerformed

